// There are a few different operators:
//
// equality:              ==
// inequality:            !=
// greater than:          >
// less than:             <
//
// There are more but this will get you pretty far.

var choice = 0;

if (choice == 0) {
  console.log("zero");
} else if (choice > 0) {
  console.log("greater than zero");
} else if (choice < 0) {
  console.log("less than zero");
}
